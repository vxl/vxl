vnl_matrix<int> reflect_about_vert(vnl_matrix<int> const& m)
{
  unsigned nc = m.cols(), nr = m.rows();
  vnl_matrix<int> ref(nr, nc);
  int rc = (nc-1)/2;
  for (unsigned r = 0; r<nr; ++r)
    for (int c = -rc; c<=rc; ++c)
      ref[r][rc+c] = m[r][rc-c];
  return ref;
}

