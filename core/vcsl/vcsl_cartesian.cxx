// This is vxl/vcsl/vcsl_cartesian.cxx
#include "vcsl_cartesian.h"

//***************************************************************************
// Constructors/Destructor
//***************************************************************************

//---------------------------------------------------------------------------
// Default constructor. Set itself right handed
//---------------------------------------------------------------------------
vcsl_cartesian::vcsl_cartesian(void)
{
  right_handed_=true;
}

//---------------------------------------------------------------------------
// Destructor
//---------------------------------------------------------------------------
vcsl_cartesian::~vcsl_cartesian()
{
}

//***************************************************************************
// Status report
//***************************************************************************

//---------------------------------------------------------------------------
// Are the axes of `this' right handed ?
//---------------------------------------------------------------------------
bool vcsl_cartesian::is_right_handed(void) const
{
  return right_handed_;
}

//***************************************************************************
// Status setting
//***************************************************************************

//---------------------------------------------------------------------------
// Set whether the coordinate system is right handed or not
//---------------------------------------------------------------------------
void vcsl_cartesian::set_right_handed(const bool new_right_handed)
{
  right_handed_=new_right_handed;
}
