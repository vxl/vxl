{
  return 1;
}

//***************************************************************************
// Singleton pattern
//***************************************************************************

//---------------------------------------------------------------------------
// Return the reference to the unique vcsl_radian object
//---------------------------------------------------------------------------
vcsl_radian_sptr vcsl_radian::instance(void)
{
  if (instance_.ptr()==0)
    instance_=new vcsl_radian;
  return instance_;
}

